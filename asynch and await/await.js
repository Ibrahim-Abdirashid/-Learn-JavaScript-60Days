/*1: Waa maxay async iyo await?
(async) iyo (await) waa habab fudud oo lagu maamulo shaqooyinka (asynchronous) ee JavaScript. 
Waxay ka dhigaan code-ka (asynchronous) mid u muuqda sidii mid (synchronous), 
taas oo fududeynaysa fahamka iyo qorista code-ka.

2: Sidey u shaqeeyaan async iyo await?
Talaabooyinka:
  -Marka aad (async function) isticmaasho:

    => Waxaad ku sheegaysaa JavaScript in shaqadan ay noqon doonto mid (asynchronous).

  -Marka aad (await) isticmaasho:
     =>Waxay sugeysaa in Promise-ka uu dhammaado ka hor inta aysan code-ku socon.
     =>Haddii Promise uu guuleysto (resolve), await waxay soo celinaysaa natiijada.
     =>Haddii Promise uu fashilmo (reject), waxaa laga qaban karaa khaladka adoo adeegsanaya (try)...(catch). */